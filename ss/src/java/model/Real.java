package model;

/**
 *
 * @author MR1
 */
public class Real {
 int id;
 String name;
 int age;
 String address;
 int pincode;
    public Real() {
    }

    public Real(int id, String name, int age, String address, int pincode) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.address = address;
        this.pincode = pincode;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getPincode() {
        return pincode;
    }

    public void setPincode(int pincode) {
        this.pincode = pincode;
    }

    
}
