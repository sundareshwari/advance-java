package model;

/**
 *
 * @author MR1
 */
public class Product {
    int pid;
    String pname;
    String pdesc;
    int price;

    public Product() {
    }

    public Product(int pid, String pname, String pdesc, int price) {
        this.pid = pid;
        this.pname = pname;
        this.pdesc = pdesc;
        this.price = price;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getPdesc() {
        return pdesc;
    }

    public void setPdesc(String pdesc) {
        this.pdesc = pdesc;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
    
}
